const RCON = [0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36];
const BLOCK_SIZE = 16;

function subBytes(state) {
  for (let i = 0; i < 16; i++) state[i] = S_BOX[state[i]];
}

function invSubBytes(state) {
  for (let i = 0; i < 16; i++) state[i] = INV_S_BOX[state[i]];
}

function shiftRows(state) {
  const s = new Uint8Array(16);
  s[0] = state[0]; s[1] = state[5]; s[2] = state[10]; s[3] = state[15];
  s[4] = state[4]; s[5] = state[9]; s[6] = state[14]; s[7] = state[3];
  s[8] = state[8]; s[9] = state[13]; s[10] = state[2]; s[11] = state[7];
  s[12] = state[12]; s[13] = state[1]; s[14] = state[6]; s[15] = state[11];
  for (let i = 0; i < 16; i++) state[i] = s[i];
}

function invShiftRows(state) {
  const s = new Uint8Array(16);
  s[0] = state[0]; s[1] = state[13]; s[2] = state[10]; s[3] = state[7];
  s[4] = state[4]; s[5] = state[1]; s[6] = state[14]; s[7] = state[11];
  s[8] = state[8]; s[9] = state[5]; s[10] = state[2]; s[11] = state[15];
  s[12] = state[12]; s[13] = state[9]; s[14] = state[6]; s[15] = state[3];
  for (let i = 0; i < 16; i++) state[i] = s[i];
}

function xtime(a) { return ((a << 1) ^ ((a & 0x80) ? 0x1b : 0)) & 0xFF; }

function mixColumns(state) {
  for (let c = 0; c < 4; c++) {
    const i = c * 4;
    const a0 = state[i], a1 = state[i+1], a2 = state[i+2], a3 = state[i+3];
    const t = a0 ^ a1 ^ a2 ^ a3;
    const u = a0;
    state[i]   ^= t ^ xtime(a0 ^ a1);
    state[i+1] ^= t ^ xtime(a1 ^ a2);
    state[i+2] ^= t ^ xtime(a2 ^ a3);
    state[i+3] ^= t ^ xtime(a3 ^ u);
  }
}

function invMixColumns(state) {
  for (let c = 0; c < 4; c++) {
    const i = c * 4;
    const a0 = state[i], a1 = state[i+1], a2 = state[i+2], a3 = state[i+3];
    const u = xtime(xtime(a0 ^ a2));
    const v = xtime(xtime(a1 ^ a3));
    state[i]   ^= u;
    state[i+1] ^= v;
    state[i+2] ^= u;
    state[i+3] ^= v;
  }
  mixColumns(state);
}

function addRoundKey(state, roundKey) {
  for (let i = 0; i < 16; i++) state[i] ^= roundKey[i];
}

function keyExpansion(key) {
  const Nk = 4;
  const Nr = 10;
  const w = new Uint8Array(16 * (Nr + 1));
  w.set(key.slice(0,16), 0);
  let bytesGenerated = 16;
  let rconIter = 0;
  const temp = new Uint8Array(4);
  while (bytesGenerated < 16 * (Nr + 1)) {
    for (let i = 0; i < 4; i++) temp[i] = w[bytesGenerated - 4 + i];
    if (bytesGenerated % 16 === 0) {
      const t = temp[0]; temp[0] = temp[1]; temp[1] = temp[2]; temp[2] = temp[3]; temp[3] = t;
      temp[0] = S_BOX[temp[0]];
      temp[1] = S_BOX[temp[1]];
      temp[2] = S_BOX[temp[2]];
      temp[3] = S_BOX[temp[3]];
      temp[0] ^= RCON[rconIter++];
    }
    for (let i = 0; i < 4; i++) {
      w[bytesGenerated] = w[bytesGenerated - 16] ^ temp[i];
      bytesGenerated++;
    }
  }
  const roundKeys = [];
  for (let r = 0; r <= Nr; r++) {
    roundKeys.push(w.slice(r * 16, r * 16 + 16));
  }
  return roundKeys;
}

function encryptBlock(input16, roundKeys) {
  const state = new Uint8Array(input16);
  addRoundKey(state, roundKeys[0]);
  for (let round = 1; round <= 9; round++) {
    subBytes(state);
    shiftRows(state);
    mixColumns(state);
    addRoundKey(state, roundKeys[round]);
  }
  subBytes(state);
  shiftRows(state);
  addRoundKey(state, roundKeys[10]);
  return state;
}

function decryptBlock(input16, roundKeys) {
  const state = new Uint8Array(input16);
  addRoundKey(state, roundKeys[10]);
  invShiftRows(state);
  invSubBytes(state);
  for (let round = 9; round >= 1; round--) {
    addRoundKey(state, roundKeys[round]);
    invMixColumns(state);
    invShiftRows(state);
    invSubBytes(state);
  }
  addRoundKey(state, roundKeys[0]);
  return state;
}

const T_C_KEY = Uint8Array.from([0xFA,0xD3,0x25,0x72,0x81,0x29,0x00,0x00,0x00,0x00,0x00,0x00,0x3A,0xB4,0x5A,0x65]);
const TOKEN_SIZE = 23;
const TIMESTAMP_OFFSET = 2;

function _step_1(sessionTokenBuf, phoneNumber) {
  const key = new Uint8Array(T_C_KEY);
  const be8 = new Uint8Array(8);
  let pn = BigInt(phoneNumber);
  for (let i = 7; i >= 0; i--) { be8[i] = Number(pn & 0xFFn); pn >>= 8n; }
  for (let i = 0; i < 6; i++) key[6 + i] = be8[2 + i];
  const roundKeys = keyExpansion(key);
  return encryptBlock(sessionTokenBuf, roundKeys);
}

function _step_2(resultFromStep1, timestamp_ms, timestamp_offset) {
  const next_state = new Uint8Array(16);
  next_state[1] = 0x0A;
  next_state[2] = 0x0A;
  let ts = Number((BigInt(timestamp_ms) + BigInt(timestamp_offset)) & 0xFFFFFFFFn);
  next_state[10] = (ts >>> 24) & 0xFF;
  next_state[11] = (ts >>> 16) & 0xFF;
  next_state[12] = (ts >>> 8) & 0xFF;
  next_state[13] = ts & 0xFF;
  const roundKeys = keyExpansion(resultFromStep1);
  return decryptBlock(next_state, roundKeys);
}

function normalizeTokenType(tokenType) {
  if (typeof tokenType === 'string') {
    const t = tokenType.toLowerCase();
    if (t === 'sms') return 'SMS';
    if (t === 'primary') return 'PRIMARY';
    if (t === 'secondary') return 'SECONDARY';
  }
  if (typeof tokenType === 'number') {
    if (tokenType === 0) return 'SMS';
    if (tokenType === 1) return 'PRIMARY';
    if (tokenType === 2) return 'SECONDARY';
  }
  return tokenType;
}

function hexToBytes(hex) {
  const normalized = hex.trim();
  if (normalized.length % 2 !== 0) {
    throw new Error('Invalid hex string length');
  }
  const bytes = new Uint8Array(normalized.length / 2);
  for (let i = 0; i < bytes.length; i++) {
    const byte = Number.parseInt(normalized.slice(i * 2, i * 2 + 2), 16);
    if (Number.isNaN(byte)) throw new Error('Invalid hex string');
    bytes[i] = byte;
  }
  return bytes;
}

function bytesToHex(bytes) {
  return Array.from(bytes)
    .map((b) => b.toString(16).padStart(2, '0'))
    .join('')
    .toUpperCase();
}

function generateToken(sessionTokenHex, phoneNumber, tokenType, options = {}) {
  if (typeof sessionTokenHex !== 'string') throw new Error('sessionTokenHex must be a hex string');
  const sessionTokenBuf = hexToBytes(sessionTokenHex);
  if (sessionTokenBuf.length !== 16) throw new Error('Invalid session token length');
  const phoneNum = BigInt(typeof phoneNumber === 'string' ? phoneNumber : String(phoneNumber));
  const timestamp_ms = options.timestampMs ?? Math.floor(Date.now() / 1000);
  const timestamp_offset = options.timestampOffset ?? TIMESTAMP_OFFSET;
  const step2_key = _step_1(sessionTokenBuf, phoneNum);
  const step2_result = _step_2(step2_key, timestamp_ms, timestamp_offset);

  const result = new Uint8Array(TOKEN_SIZE);
  const tt = normalizeTokenType(tokenType);
  if (tt === 'SMS') result[0] = 0x01;
  else if (tt === 'PRIMARY') result[0] = 0x11;
  else if (tt === 'SECONDARY') result[0] = 0x21;
  else throw new Error('unknown token type: ' + tokenType);

  const phoneNumBI = phoneNum;
  const shift = (n) => Number((phoneNumBI >> BigInt(n)) & 0xFFn);
  result[1] = shift(0x28);
  result[2] = shift(0x20);
  result[3] = shift(0x18);
  const be8 = new Uint8Array(8);
  let pn = phoneNumBI;
  for (let i = 7; i >= 0; i--) { be8[i] = Number(pn & 0xFFn); pn >>= 8n; }
  for (let i = 0; i < 3; i++) result[4 + i] = be8[5 + i];
  for (let i = 0; i < 16; i++) result[7 + i] = step2_result[i];
  return bytesToHex(result);
}

